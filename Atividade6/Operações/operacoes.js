val_1 = prompt("Digite seu 1° Numero?");
val_2 = prompt("Digite seu 2° Numero?");

alert(`${val_1} + ${val_2} = ${parseFloat(val_1) + parseFloat(val_2)}`);
alert(`${val_1} - ${val_2} = ${val_1 - val_2}`);
alert(`${val_1} * ${val_2} = ${val_1 * val_2}`);
alert(`${val_1} / ${val_2} = ${val_1 / val_2}`);
alert(`${val_1} % ${val_2} = ${val_1 % val_2}`);